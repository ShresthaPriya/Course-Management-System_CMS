    private void deleteCourseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteCourseButtonActionPerformed

        try {

            String id = JOptionPane.showInputDialog("Enter Course ID to Delete !");

            java.sql.Connection conn = Connection.getConnection();
            String sql = "DELETE FROM COURSE WHERE ID = ?";

            PreparedStatement preparedStmt = conn.prepareStatement(sql);
            preparedStmt.setString(1, id);

            boolean success = preparedStmt.execute();
            System.out.println(success);

            if (!success) {
                JOptionPane.showMessageDialog(this, "Course Successfully Deleted!");
                dispose();
            }
            conn.close();

        } catch (Exception e) {
            System.out.println("EXCEPTION:: " + e.getMessage());
        }// TODO add your handling code here:
    }//GEN-LAST:event_deleteCourseButtonActionPerformed

